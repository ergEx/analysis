plot(sim.H0, n.min=20, boundary=c(10), n.trajectories = 100)
plot(sim.H1, n.min=20, boundary=c(10), n.trajectories = 100)
plot(sim.H0, n.min=20, n.max=100, boundary=c(10), forH1 = FALSE)
